            <p>La <strong>abeja europea (Apis mellifera)</strong>, también conocida como <strong>abeja doméstica</strong> o <strong>abeja melífera</strong>, es una especie de himenóptero apócrito de la familia Apidae. Es la especie de <strong>abeja</strong> con mayor distribución en el mundo. Originaria de Europa, África y parte de Asia, fue introducida en América y Oceanía. Fue clasificada por Carlos Linneo en 1758. </p>
              <p>A partir de entonces numerosos taxónomos describieron variedades geográficas o subespecies que, en la actualidad, superan las treinta razas. Actualmente la población de abejas se halla en franco retroceso sin que se conozca de manera clara las causas, que bien podría ser uncúmulo de diversos factores.</p>
