  <h1><strong> <u>Su importancia en el medio.</u></strong></h1>
  <p style="color: #ffdd90";>H</p>
<strong>Einstein lo dejó claro "si las abejas desaparecen del planeta solo nos quedarían 4 años de vida"</strong>
 
 <p>El papel que juegan las <strong>abejas</strong> en el medio ambiente es fundamental ya que su labor de polinización es insustituible. Sin polinizadores un tercio de nuestra alimentación desaparecería, y lo que es aún más grave, también lo haría parte del forraje que alimenta a los ganados que comemos.
 Desde hace unos años la alarma no cesa, se están perdiendo las colonias de <strong>abejas</strong>. Las causas de su muerte son diversas, la contaminación del aire, tanto por el porcentaje de CO2 como por las ondas magnéticas enviadas por las antenas de telefonía móvil. </p>

<p>La <strong>abeja</strong> es un auténtico detector ecológico porque su cuerpo esta cubierto de pelos que ayudan a capturar las substancias que entran en contacto con ellos, es sensible a muchos productos tóxicos y visitan todos los sectores ambientales: suelo, vegetación, aire y agua, y almacenan gran diversidad de productos en las colmenas sobre las que realizar las verificaciones. </p>

<p>Las <strong>abejas</strong> proveen un servicio ecosistémico esencial para la vida y el Medio Ambiente, ya que son grandes polinizadores, por lo que tienen un papel importante en la producción de alimentos y en la seguridad alimentaria en el mundo.</p>
<p><img src="imagenes/abeja pagina 2.jpg" class="rounded float-left" alt="Abeja Obrera" width=200px height=150px></p>


<p>Las <strong>abejas</strong> recogen el néctar de las flores para utilizarlo como alimento o convertirlo más tarde en miel, es en este proceso donde el polen se pega al cuerpo de las <strong>abejas</strong> y se transfiere desde los estambres hasta el estigma de las flores, donde germina y fecunda los óvulos de la flor, haciendo posible la generación de nuevas semillas y frutos.</p>

<p>Los polinizadores como las <strong>abejas</strong> inciden sobre el 35% de la producción agrícola mundial, aumentando el rendimiento de 87 de los principales cultivos de todo el mundo, así como de numerosas medicinas de origen vegetal.</p>

<p>Las <strong>abejas</strong> ayudan a mantener la biodiversidad en el planeta, ya que la polinización contribuye a la reproducción de una gran variedad de plantas, lo que permite el crecimiento de nuevos árboles y la regeneración de bosques, los que a su vez contribuyen a conservar la biodiversidad, a captar el dióxido de carbono de la atmósfera y al sustento de animales y aves.</p>
